// create trees for stage 1
export const treeOne = (element) => {
    let obstacleX = [];
    let obstacleY = [];

    element.style.top = '50px';
    element.style.left = '50px';
    element.style.width = '50px';
    element.style.height = '50px';
    element.style.backgroundImage = 'url("./assets/trees/tree-1.png")';

    for (let x = 30; x <= 70; x += 10) {
        for (let y = 30; y <= 70; y += 10) {
            obstacleX.push(x);
            obstacleY.push(y);
        }
    }

    return {
        "element": element,
        "obstacleX": obstacleX,
        "obstacleY": obstacleY
    };
}

